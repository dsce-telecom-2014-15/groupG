
public class Calculations {

	public int addition(int a,int b){
		return a+b;
		
	}
 public int subtraction(int a,int b){
	 return a-b;
	 
 }
 public int multiplication(int a,int b){
	return a*b;
}
 public int quotient(int a,int b){
	return a/b;
}
 public int remainder(int a,int b){
	return a%b;
}
}